

/**
    * Enum representing ANSI escape codes for text formatting in the terminal.
    * @readonly
*/

export enum ANSI_ESCAPE_CODE {

    /**
        * Resets all formatting to default.
    */

    RESET = '\u001B[0m',

    /**
        * Sets the background color to blue.
    */

    BG_BLUE = '\u001B[44m'

}